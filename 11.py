__doc__ = '''Read 2 numbers to variable a and b and perform all bitwise operations 
        on that numbers.'''

a = 100
b = 40
    
# Print bitwise AND operation    
print("a & b =", a & b)  
    
# Print bitwise OR operation  
print("a | b =", a | b)  
    
# Print bitwise NOT operation   
print("~a =", ~a)  
    
# print bitwise XOR operation   
print("a ^ b =", a ^ b) 

a = 100
b = -100
  
# print bitwise right shift operator 
print("a >> 1 =", a >> 1) 
print("b >> 1 =", b >> 1) 
  
a = 50
